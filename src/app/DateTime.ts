export class DateTime {

    year: string;
    month: string;
    day: string;
    hours: string;
    minutes: string;
    seconds: string;

    constructor(year: string, month: string, day: string, hours: string, minutes: string, seconds: string) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }

}